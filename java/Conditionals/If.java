public class If{
    public static void main (String[]args){
        int Salary=10000;
        if (Salary>20000) {
            System.out.println("Salary okay");
        } else if (Salary<15000 ){
            System.out.println("Salary Not Okay");
        }else{
            System.out.println("Salary bad");
        }
    }
}